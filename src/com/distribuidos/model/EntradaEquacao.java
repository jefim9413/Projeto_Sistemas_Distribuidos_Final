
package com.distribuidos.model;

public class EntradaEquacao {
    private int a, b, c;

    public EntradaEquacao(int a, int b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int getA() { return a; }
    public int getB() { return b; }
    public int getC() { return c; }
}
